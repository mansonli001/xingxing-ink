/** 根据 mode + trigger 文本快速查找预制回复（O(n) n=3 可接受） */
export function findPreset(
  mode: ModeId,
  trigger: string
): PresetReply | null {
  const list = PRESET_REPLIES[mode];
  if (!list) return null;
  return list.find((p) => p.trigger === trigger) || null;
}
